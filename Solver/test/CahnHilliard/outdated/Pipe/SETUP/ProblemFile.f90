         FUNCTION RandomNormal(mean,sigma)
            ! RandomNormal function taken from a StackOverflow post

            ! mean  : mean of distribution
            ! sigma : number of standard deviations

            USE SMConstants

            IMPLICIT NONE

            REAL(KIND=RP) :: RandomNormal
            REAL(KIND=RP) :: mean
            REAL(KIND=RP) :: sigma

            REAL          :: rand_num
            REAL(KIND=RP) :: tmp
            REAL(KIND=RP) :: fac
            REAL(KIND=RP) :: gsave
            REAL(KIND=RP) :: rsq
            REAL(KIND=RP) :: r1
            REAL(KIND=RP) :: r2
            INTEGER       :: flag

            SAVE :: flag
            SAVE :: gsave
            DATA flag /0/


            IF (flag .EQ. 0) THEN
                  rsq = 2.0_RP

                  DO WHILE(rsq .GE. 1.0_RP .OR. rsq .EQ. 0.0_RP)
                        CALL RANDOM_NUMBER(rand_num)
                        r1  = 2.0_RP * rand_num - 1.0_RP
                        CALL RANDOM_NUMBER(rand_num)
                        r2  = 2.0_RP * rand_num - 1.0_RP
                        rsq = r1 * r1 + r2 * r2
                  ENDDO

                  fac   = SQRT( -2.0_RP * LOG( rsq ) / rsq )
                  gsave = r1 * fac
                  tmp   = r2 * fac
                  flag  = 1
            ELSE
                  tmp   = gsave
                  flag  = 0
            ENDIF

            RandomNormal = tmp * sigma + mean

            RETURN

            END FUNCTION RandomNormal

      